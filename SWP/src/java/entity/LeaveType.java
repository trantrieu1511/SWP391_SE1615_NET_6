package entity;

/**
 *
 * @author DELL
 */
public class LeaveType {

    int id;
    String name;

    public LeaveType() {
    }

    public LeaveType(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return "LeaveType{" + "id=" + id + ", name=" + name + '}';
    }

}
